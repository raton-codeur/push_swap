#include "../libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*result;
	size_t	i;
	size_t	length_s;

	if (s == NULL)
		return (NULL);
	length_s = ft_strlen(s);
	if ((size_t)start >= length_s)
		return (ft_strdup(""));
	if (length_s - start < len)
		len = length_s - start;
	result = malloc(sizeof(char) * (len + 1));
	if (result == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		result[i] = s[start + i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
